    getPhotosByArtist: (artistName) => {
        return PORTFOLIO_DATA.photos.filter(photo => 
            photo.title.includes(artistName.toUpperCase())
        );
    },
    
    // Obtenir toutes les photos d'une ann√©e
    getPhotosByYear: (year) => {
        return PORTFOLIO_DATA.photos.filter(photo => photo.year === year);
    },
    
    // Obtenir toutes les photos d'une cat√©gorie
    getPhotosByCategory: (category) => {
        return PORTFOLIO_DATA.photos.filter(photo => photo.category === category);
    },
    
    // Rechercher des photos par tag
    searchByTag: (tag) => {
        return PORTFOLIO_DATA.photos.filter(photo => 
            photo.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
        );
    },
    
    // Obtenir des statistiques
    getStats: () => {
        const stats = {
            totalPhotos: PORTFOLIO_DATA.photos.length,
            artists: [...new Set(PORTFOLIO_DATA.photos.map(p => p.title))],
            years: [...new Set(PORTFOLIO_DATA.photos.map(p => p.year))],
            categories: [...new Set(PORTFOLIO_DATA.photos.map(p => p.category))]
        };
        return stats;
    }
